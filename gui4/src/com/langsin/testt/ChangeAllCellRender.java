package com.langsin.testt;
import javax.swing.*;
import javax.swing.tree.*;
import java.awt.*;
public class ChangeAllCellRender {
	JFrame jf = new JFrame("改变所有节点的外观");

	JTree tree;
	// 定义几个初始节点
	DefaultMutableTreeNode root = new DefaultMutableTreeNode("中国");
	DefaultMutableTreeNode shandong = new DefaultMutableTreeNode("山东");
	DefaultMutableTreeNode jiangsu = new DefaultMutableTreeNode("江苏");
	DefaultMutableTreeNode jinan = new DefaultMutableTreeNode("济南");
	DefaultMutableTreeNode qingdao = new DefaultMutableTreeNode("青岛");
	DefaultMutableTreeNode nanjing = new DefaultMutableTreeNode("南京");
	DefaultMutableTreeNode suzhou = new DefaultMutableTreeNode("苏州");

	public void init() throws Exception {
		// 通过add方法建立树节点之间的父子关系
		shandong.add(jinan);
		shandong.add(qingdao);
		jiangsu.add(nanjing);
		jiangsu.add(suzhou);
		root.add(shandong);
		root.add(jiangsu);
		// 以根节点创建树
		tree = new JTree(root);

		// 创建一个DefaultTreeCellRender对象
		DefaultTreeCellRenderer cellRender = new DefaultTreeCellRenderer();
		// 设置非选定节点的背景色。
		cellRender.setBackgroundNonSelectionColor(new Color(255,255,0));
		// 设置节点在选中状态下的背景颜色。
		cellRender.setBackgroundSelectionColor(new Color(0, 0, 255));
		// 设置选中状态下节点的边框颜色。
		cellRender.setBorderSelectionColor(Color.BLACK);
		// 设置处于折叠状态下非叶子节点的图标。
		cellRender.setClosedIcon(new ImageIcon("ico/close.gif"));
		// 设置节点文本的字体。
		cellRender.setFont(new Font("SansSerif", Font.BOLD, 12));
		// 设置叶子节点的图标。
		cellRender.setLeafIcon(new ImageIcon("ico/leaf.png"));
		// 设置处于展开状态下非叶子节点的图标。
		cellRender.setOpenIcon(new ImageIcon("ico/open.gif"));
		// 设置绘制非选中状态下节点文本的颜色。
		cellRender.setTextNonSelectionColor(new Color(255, 0, 0));
		// 设置绘制选中状态下节点文本的颜色。
		cellRender.setTextSelectionColor(new Color(0, 255, 0));

		tree.setCellRenderer(cellRender);

		// 设置是否显示根节点的“展开/折叠”图标,默认是false
		tree.setShowsRootHandles(true);
		// 设置节点是否可见,默认是true
		tree.setRootVisible(true);

		jf.add(new JScrollPane(tree));
		jf.pack();
		jf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		jf.setVisible(true);
	}

	public static void main(String[] args) throws Exception {
		new ChangeAllCellRender().init();
	}
}
